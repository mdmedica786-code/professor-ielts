import ScoreGauge from './ScoreGauge';
import MiniScoreCard from './MiniScoreCard';
import { getCriteriaName, getBandDescriptor } from '../../utils/scoring';
import { formatDuration, formatDateTime } from '../../utils/formatters';
import { useApp } from '../../context/AppContext';
import { Printer, Award, Clock, Hash, User } from 'lucide-react';

export default function ReportCard({ evaluation }) {
  const ev = evaluation;
  const { studentName } = useApp();

  const handlePrint = () => {
    const printWindow = window.open('', '_blank');
    printWindow.document.write(`
      <!DOCTYPE html>
      <html>
      <head>
        <title>IELTS Report — ${studentName || 'Student'}</title>
        <link href="https://fonts.googleapis.com/css2?family=Inter:wght@300;400;500;600;700;800;900&display=swap" rel="stylesheet">
        <style>
          * { margin: 0; padding: 0; box-sizing: border-box; font-family: 'Inter', sans-serif; }
          body { padding: 40px; background: white; color: #0f172a; }
          .header { text-align: center; border-bottom: 3px solid #9333ea; padding-bottom: 20px; margin-bottom: 30px; }
          .header h1 { font-size: 24px; color: #9333ea; font-weight: 800; }
          .header p { font-size: 12px; color: #94a3b8; text-transform: uppercase; letter-spacing: 3px; margin-top: 4px; }
          .meta { display: flex; gap: 20px; justify-content: center; margin: 15px 0; font-size: 12px; color: #64748b; }
          .overall { text-align: center; margin: 30px 0; }
          .overall .band { font-size: 64px; font-weight: 900; font-family: monospace; color: #9333ea; }
          .overall .desc { font-size: 14px; color: #64748b; }
          .scores { display: grid; grid-template-columns: repeat(3, 1fr); gap: 16px; margin: 30px 0; }
          .score-box { text-align: center; padding: 16px; border: 1px solid #e2e8f0; border-radius: 12px; }
          .score-box .val { font-size: 28px; font-weight: 800; font-family: monospace; }
          .score-box .label { font-size: 10px; text-transform: uppercase; letter-spacing: 2px; color: #94a3b8; margin-top: 4px; }
          .verdict { text-align: center; padding: 20px; background: #faf5ff; border-radius: 12px; margin: 20px 0; font-size: 14px; color: #6b21a8; line-height: 1.6; }
          .footer { text-align: center; margin-top: 40px; font-size: 10px; color: #cbd5e1; border-top: 1px solid #e2e8f0; padding-top: 15px; }
          .good { color: #059669; } .okay { color: #d97706; } .poor { color: #e11d48; }
        </style>
      </head>
      <body>
        <div class="header">
          <h1>BandLogic</h1>
          <p>Decode your IELTS · Quantify your progress</p>
        </div>
        <div class="meta">
          <span>Student: <strong>${ev.metadata?.studentName || studentName || 'Student'}</strong></span>
          <span>Date: <strong>${new Date(ev.metadata?.timestamp || Date.now()).toLocaleDateString('en-GB', { day: 'numeric', month: 'long', year: 'numeric' })}</strong></span>
          <span>Part: <strong>${ev.metadata?.questionPart || '—'}</strong></span>
        </div>
        <div class="overall">
          <div class="band ${ev.overallBand >= 7 ? 'good' : ev.overallBand >= 5.5 ? 'okay' : 'poor'}">${ev.overallBand.toFixed(1)}</div>
          <div class="desc">Overall Band — ${getBandDescriptor(ev.overallBand)}</div>
        </div>
        <div class="scores">
          ${Object.entries(ev.scores).filter(([k]) => k !== 'p').map(([k, v]) => `
            <div class="score-box">
              <div class="val ${v >= 7 ? 'good' : v >= 5.5 ? 'okay' : 'poor'}">${v.toFixed(1)}</div>
              <div class="label">${getCriteriaName(k)}</div>
            </div>
          `).join('')}
        </div>
        <div class="verdict">${ev.verdict || ''}</div>
        <div class="footer">
          Generated by BandLogic • Powered by OpenAI • ${new Date().toISOString()}
        </div>
      </body>
      </html>
    `);
    printWindow.document.close();
    setTimeout(() => printWindow.print(), 500);
  };

  return (
    <div className="space-y-5 animate-slide-up">
      {/* Print Button */}
      <div className="flex justify-end no-print">
        <button
          id="print-report"
          onClick={handlePrint}
          className="btn-secondary flex items-center gap-2"
        >
          <Printer className="w-4 h-4" />
          Print Certificate
        </button>
      </div>

      {/* Overall Band */}
      <div className="card-padded text-center">
        <div className="flex items-center justify-center gap-2 mb-4">
          <Award className="w-5 h-5 text-brand-500" />
          <h2 className="text-lg font-bold text-slate-900">IELTS Speaking Score</h2>
        </div>

        <ScoreGauge score={ev.overallBand} size={160} strokeWidth={10} />

        <p className="text-sm text-slate-600 mt-3 font-medium">
          Band {ev.overallBand.toFixed(1)} — {getBandDescriptor(ev.overallBand)}
        </p>

        {/* 3 criteria scores */}
        <div className="grid grid-cols-3 gap-3 mt-6">
          {Object.entries(ev.scores).filter(([key]) => key !== 'p').map(([key, val]) => (
            <div key={key} className="text-center">
              <div className="text-2xl font-mono font-extrabold" style={{ color: val >= 7 ? '#059669' : val >= 5.5 ? '#d97706' : '#e11d48' }}>
                {val.toFixed(1)}
              </div>
              <div className="label-caps mt-1">{getCriteriaName(key)}</div>
            </div>
          ))}
        </div>
      </div>

      {/* Verdict */}
      {ev.verdict && (
        <div className="card-padded bg-brand-50/50 border-brand-200">
          <p className="text-sm text-brand-900 leading-relaxed">{ev.verdict}</p>
        </div>
      )}

      {/* Metadata */}
      <div className="flex flex-wrap gap-4 text-xs text-slate-400">
        {ev.metadata?.studentName && (
          <span className="flex items-center gap-1">
            <User className="w-3 h-3" /> {ev.metadata.studentName}
          </span>
        )}
        {ev.metadata?.audioDuration > 0 && (
          <span className="flex items-center gap-1">
            <Clock className="w-3 h-3" /> {formatDuration(ev.metadata.audioDuration)}
          </span>
        )}
        {ev.metadata?.totalWords > 0 && (
          <span className="flex items-center gap-1">
            <Hash className="w-3 h-3" /> {ev.metadata.totalWords} words
          </span>
        )}
        {ev.metadata?.timestamp && (
          <span>{formatDateTime(ev.metadata.timestamp)}</span>
        )}
      </div>
    </div>
  );
}
